    private void loadListQuestionByTest(int Test_ID) {
        listQuestion = (ArrayList<Question>) questionDAO.getListQuestionByTestID(Test_ID);
        jTableQuestions.setModel(new CustomTableTestQuestion(listQuestion));
    }
    
    private void changeTestStatus() {
        try {
            int indexTest = jTableTest.getSelectedRow();

            if (indexTest >= 0) {
                int Test_ID = listTest.get(indexTest).getTest_ID();

                if (JOptionPane.showConfirmDialog(this, "Bạn có muốn "+jButtonChangeStatus.getText()
                        +" mã đề " + listTest.get(indexTest).getTest_Name(),
                        "Thông báo",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    if (testDAO.changeTestStatusByID(Test_ID)) {
                        loadData();
                        throw new Exception(jButtonChangeStatus.getText()+" thành công!!!");
                    } else {
                        throw new Exception(jButtonChangeStatus.getText()+" không thành công!!!");
                    }
                }
            } else {
                throw new Exception("Chưa chọn đề!!!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

