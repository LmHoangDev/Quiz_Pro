    private void jTableTestMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTestMousePressed
        // TODO add your handling code here:
        int indexTest = jTableTest.getSelectedRow();
        try {
            if (indexTest >= 0) {
                int Test_ID = listTest.get(indexTest).getTest_ID();
                loadListQuestionByTest(Test_ID);
                if (((String) jTableTest.getValueAt(indexTest, 3)).compareToIgnoreCase("Hoạt động") == 0) {
                    jButtonChangeStatus.setText("Vô hiệu hóa");
                } else {
                    jButtonChangeStatus.setText("Kích hoạt");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTableTestMousePressed

